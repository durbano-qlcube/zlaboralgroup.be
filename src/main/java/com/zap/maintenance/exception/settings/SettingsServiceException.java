package com.zap.maintenance.exception.settings;


public class SettingsServiceException extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6953124712726404950L;

	public SettingsServiceException() {
		super();
	}

	public SettingsServiceException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public SettingsServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SettingsServiceException(String arg0) {
		super(arg0);
	}

	public SettingsServiceException(Throwable arg0) {
		super(arg0);
	}
 
}