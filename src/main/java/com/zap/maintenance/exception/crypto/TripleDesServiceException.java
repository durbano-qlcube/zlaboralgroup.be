package com.zap.maintenance.exception.crypto;

public class TripleDesServiceException
    extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8644381862538294618L;

	public TripleDesServiceException() {
		super();
	}

	public TripleDesServiceException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public TripleDesServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public TripleDesServiceException(String message) {
		super(message);
	}

	public TripleDesServiceException(Throwable cause) {
		super(cause);
	}
  

}