package com.zap.maintenance.exception.crypto;



public class HashServiceException
    extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -1636706239828793317L;

	public HashServiceException() {
		super();
	}

	public HashServiceException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HashServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public HashServiceException(String message) {
		super(message);
	}

	public HashServiceException(Throwable cause) {
		super(cause);
	}


}