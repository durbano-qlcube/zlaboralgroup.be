package com.zap.maintenance.exception.geo;


public class ProvinceServiceException extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6953124712726404950L;

	public ProvinceServiceException() {
		super();
	}

	public ProvinceServiceException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public ProvinceServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ProvinceServiceException(String arg0) {
		super(arg0);
	}

	public ProvinceServiceException(Throwable arg0) {
		super(arg0);
	}
 
}