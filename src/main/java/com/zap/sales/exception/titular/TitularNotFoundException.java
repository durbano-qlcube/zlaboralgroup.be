package com.zap.sales.exception.titular;



public class TitularNotFoundException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8838134532032645758L;

	public TitularNotFoundException() {
		super();
	}

	public TitularNotFoundException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public TitularNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TitularNotFoundException(String arg0) {
		super(arg0);
	}

	public TitularNotFoundException(Throwable arg0) {
		super(arg0);
	}
 

}
