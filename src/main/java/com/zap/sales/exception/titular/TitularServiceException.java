package com.zap.sales.exception.titular;


public class TitularServiceException extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6953124712726404950L;

	public TitularServiceException() {
		super();
	}

	public TitularServiceException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public TitularServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TitularServiceException(String arg0) {
		super(arg0);
	}

	public TitularServiceException(Throwable arg0) {
		super(arg0);
	}
 
}