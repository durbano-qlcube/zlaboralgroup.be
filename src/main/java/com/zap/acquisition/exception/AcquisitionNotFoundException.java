package com.zap.acquisition.exception;



public class AcquisitionNotFoundException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8838134532032645758L;

	public AcquisitionNotFoundException() {
		super();
	}

	public AcquisitionNotFoundException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public AcquisitionNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AcquisitionNotFoundException(String arg0) {
		super(arg0);
	}

	public AcquisitionNotFoundException(Throwable arg0) {
		super(arg0);
	}
 

}
