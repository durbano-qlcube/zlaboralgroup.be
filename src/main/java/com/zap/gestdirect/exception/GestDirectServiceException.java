package com.zap.gestdirect.exception;


public class GestDirectServiceException extends java.lang.RuntimeException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6953124712726404950L;

	public GestDirectServiceException() {
		super();
	}

	public GestDirectServiceException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public GestDirectServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GestDirectServiceException(String arg0) {
		super(arg0);
	}

	public GestDirectServiceException(Throwable arg0) {
		super(arg0);
	}
 
}