package com.zap.gestdirect.exception;



public class GestdirectNotFoundException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8838134532032645758L;

	public GestdirectNotFoundException() {
		super();
	}

	public GestdirectNotFoundException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public GestdirectNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GestdirectNotFoundException(String arg0) {
		super(arg0);
	}

	public GestdirectNotFoundException(Throwable arg0) {
		super(arg0);
	}
 

}
