package com.zap.ocm.exception.ocm;



public class OcmNotFoundException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8838134532032645758L;

	public OcmNotFoundException() {
		super();
	}

	public OcmNotFoundException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public OcmNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public OcmNotFoundException(String arg0) {
		super(arg0);
	}

	public OcmNotFoundException(Throwable arg0) {
		super(arg0);
	}
 

}
